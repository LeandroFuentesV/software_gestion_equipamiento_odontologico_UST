    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        btnAceptar.setBackground(new Color(0, 119, 103));
        // Verificar si el campo txtID est√° vac√≠o antes de convertirlo en un entero
        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar el ID del equipo (Escanea el c√≥digo de barras o ingr√©salo manualmente)", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        long id;
        try {
            id = Long.parseLong(txtID.getText());  // Intentar convertir el ID
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un n√∫mero v√°lido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

// Verificar si el equipo ya existe
        DAO dao = new DAO();
        if (dao.existeEquipo(id)) {
            JOptionPane.showMessageDialog(this, "El equipo con este ID ya est√° registrado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String estado = "Disponible";
        String condicion = "";

// Verificar qu√© RadioButton est√° seleccionado
        if (rdoBueno.isSelected()) {
            condicion = rdoBueno.getText();
        } else if (bntMantecion.isSelected()) {
            condicion = bntMantecion.getText();
        } else {
            JOptionPane.showMessageDialog(this, "Debes seleccionar la condici√≥n del equipo", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

// Insertar en la base de datos
        if (dao.insertarEquipo(id, estado, condicion)) {
            JOptionPane.showMessageDialog(this, "Equipo insertado correctamente.");

            DefaultTableModel modelo = dao.mostrarEquipos();
            frmEquipos.getInstancia().mostrarEquipos();
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error al insertar el equipo.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_btnAceptarActionPerformed

