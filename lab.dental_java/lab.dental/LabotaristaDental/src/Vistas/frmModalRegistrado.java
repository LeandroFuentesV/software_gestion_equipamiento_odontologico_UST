    public void setEquipoInfo(long idEquipo, String rutUsuario) {

        DAO dao = new DAO();
        dao.obtenerDatosUsuario(rutUsuario);
     

        String[] datosUsuario = dao.obtenerDatosUsuario(rutUsuario);

// Verifica si se obtuvieron datos
        if (datosUsuario[0] != null) {
            // Muestra los datos obtenidos
             lblNombreEstudiante.setText(datosUsuario[0]);
   
           
        } else {
            System.out.println("No se encontraron datos para el RUT proporcionado.");
        }
        lblNumEquipo.setText(String.valueOf(idEquipo));
        lblRutEstudiante.setText(rutUsuario);

    }

