    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        btnFiltrar.setBackground(new Color(0, 119, 103));

        if (txtRUT == null || txtCodigo == null || tblPrestamos == null) {
            JOptionPane.showMessageDialog(this, "Error: Un componente necesario no está inicializado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        btnFiltrar.setBackground(new Color(0, 119, 103)); // Cambiar el color del botón

        String rut = txtRUT.getText().trim();
        String codigo = txtCodigo.getText().trim();

        if (!rut.isEmpty() && codigo.isEmpty()) {
            try {
                filtrarPorRut(rut); // Llama al método para filtrar por RUT
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al filtrar por RUT: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else if (!codigo.isEmpty() && rut.isEmpty()) {
            try {
                filtrarPorEquipo(codigo); // Llama al método para filtrar por Código de Equipo
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al filtrar por Código de Equipo: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else if (!rut.isEmpty() && !codigo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese solo uno de los campos para filtrar (RUT o Código de Equipo).", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Debe ingresar al menos un campo para filtrar.", "Error", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_btnFiltrarActionPerformed

