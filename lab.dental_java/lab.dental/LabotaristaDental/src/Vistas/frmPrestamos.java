    // Método para mostrar todos los registros
    public void mostrarRegistroPrestamos() {
        DAO prestamos = new DAO();
        DefaultTableModel modelo = prestamos.mostrarRegistroPrestamos();
        tblPrestamos.setModel(modelo);
    }

// Método para inicializar los filtros de RUT y equipo
    public void inicializarFiltros() {
        btnFiltrar.addActionListener(e -> {
            String rut = txtRUT.getText().trim();
            String codigo = txtCodigo.getText().trim();

            if (!rut.isEmpty()) {
                filtrarPorRut(rut);
            } else if (!codigo.isEmpty()) {
                filtrarPorEquipo(codigo);
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese un RUT o código de equipo para filtrar.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        });
    }

// filtrar por RUT
    private void filtrarPorRut(String rut) {
        DAO dao = new DAO();
        DefaultTableModel modelo = dao.filtrarPorRut(rut);
        tblPrestamos.setModel(modelo);
    }

// filtrar por equipo
    private void filtrarPorEquipo(String codigoEquipo) {
        try {
            Long id = Long.parseLong(codigoEquipo);
            DAO dao = new DAO();
            DefaultTableModel modelo = dao.filtrarPorEquipo(id);
            tblPrestamos.setModel(modelo);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El código de equipo debe ser un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

// exportar los datos de la tabla
    private void exportarDatos() {
        Exportar exportar = new Exportar();
        try {
            exportar.exportarExcel(tblPrestamos);
            JOptionPane.showMessageDialog(this, "Datos exportados exitosamente.");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error al exportar los datos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

