      public void setEquipoInfo(long idEquipo, String rutUsuario) {
           DAO dao = new DAO();
        dao.obtenerDatosUsuario(rutUsuario);
        dao.obtenerInfoPrestamo(idEquipo);
        
        String[] RutPrestamo = dao.obtenerInfoPrestamo(idEquipo);
        String rutUsuarioPrestamoActual=RutPrestamo[0];
        String[] datosUsuarioPrestamoActual = dao.obtenerDatosUsuario(rutUsuarioPrestamoActual);


        if (datosUsuarioPrestamoActual[0] != null) {
            // Muestra los datos obtenidos
             lblNombreEstudiante.setText(datosUsuarioPrestamoActual[0]);
   
           
        } else {
            System.out.println("No se encontraron datos para el RUT proporcionado.");
        }
        lblNumEquipo.setText(String.valueOf(idEquipo));
        txtRUT.setText(rutUsuario);
    }
      
