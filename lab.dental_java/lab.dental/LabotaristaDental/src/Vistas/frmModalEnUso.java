    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
        btnDevolver.setBackground(new Color(0, 119, 103));
         String rutEstudiante = txtRUT.getText();
        long idEquipo;

        try {
            // Parsear el ID del equipo a un entero
            idEquipo = Long.parseLong(lblNumEquipo.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID del equipo debe ser un número válido.");
            return;
        }

        DAO dao = new DAO();

        boolean exito= dao.devolverEquipo(rutEstudiante, idEquipo);
        if (exito) {
            JOptionPane.showMessageDialog(this, "Devolución realizada con éxito.");
            this.dispose();
            
        }
    }//GEN-LAST:event_btnDevolverActionPerformed

